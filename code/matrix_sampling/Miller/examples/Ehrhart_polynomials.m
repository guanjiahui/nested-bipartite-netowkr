% Values of the Ehrhart polynomials H_n(r)
%   for r=1,..,nchoosek(n-1,2) for n=4,..,8.


for n = 4:8
    fprintf('n = %d\n',n);
    fprintf('r = 1,...,%d\n',nchoosek(n-1,2));
    total_time = 0;
    for r = 1:nchoosek(n-1,2)
        tic;
        p = r*ones(1,n);
        number = count(p,p,1);
        this_time = toc;
        fprintf('(r=%d) %s  (%.1f sec)\n',r,number,this_time);
        total_time = total_time + this_time;
    end
    fprintf('Total elapsed time for n = %d is %.1f seconds\n',n,total_time);
end


